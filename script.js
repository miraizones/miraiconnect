// 3. 送金確定とタイマー開始
function confirmRemitFinal() {
    remitStartTime = Date.now();
    document.getElementById('transfer-step-2').style.display = 'none';
    document.getElementById('transfer-step-3').style.display = 'block';
    
    startRemitCountdown();
}

function startRemitCountdown() {
    let secondsLeft = 300; // 5分 = 300秒
    
    if (remitTimerInterval) clearInterval(remitTimerInterval);
    
    remitTimerInterval = setInterval(() => {
        secondsLeft--;
        const m = Math.floor(secondsLeft / 60);
        const s = secondsLeft % 60;
        document.getElementById('remit-timer').innerText = 
            `${m.toString().padStart(2, '0')}:${s.toString().padStart(2, '0')}`;
        
        if (secondsLeft <= 0) {
            clearInterval(remitTimerInterval);
            document.getElementById('timer-area').innerHTML = 
                "<p style='color:#7f8c8d;'>キャンセル可能時間を過ぎました。送金が最終確定されました。</p>";
        }
    }, 1000);
}

// 4. キャンセル処理
function processCancellation() {
    const now = Date.now();
    const diffSeconds = (now - remitStartTime) / 1000;

    if (diffSeconds <= 300) {
        clearInterval(remitTimerInterval);
        alert("送金は正常にキャンセルされました。返金処理を行います。");
        closeTransferSystem();
    } else {
        alert("5分を経過しているため、キャンセルできません。");
    }
}

